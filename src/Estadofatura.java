public class Estadofatura {

  private long numfatura;
  private long idestado;
  private java.sql.Date datafatura;


  public long getNumfatura() {
    return numfatura;
  }

  public void setNumfatura(long numfatura) {
    this.numfatura = numfatura;
  }


  public long getIdestado() {
    return idestado;
  }

  public void setIdestado(long idestado) {
    this.idestado = idestado;
  }


  public java.sql.Date getDatafatura() {
    return datafatura;
  }

  public void setDatafatura(java.sql.Date datafatura) {
    this.datafatura = datafatura;
  }

}
