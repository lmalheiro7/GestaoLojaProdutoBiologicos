package com.sample;


public class Linhaencomendafornecedor {

  private long numproduto;
  private long numencomenda;
  private double valor;
  private long quantidade;


  public long getNumproduto() {
    return numproduto;
  }

  public void setNumproduto(long numproduto) {
    this.numproduto = numproduto;
  }


  public long getNumencomenda() {
    return numencomenda;
  }

  public void setNumencomenda(long numencomenda) {
    this.numencomenda = numencomenda;
  }


  public double getValor() {
    return valor;
  }

  public void setValor(double valor) {
    this.valor = valor;
  }


  public long getQuantidade() {
    return quantidade;
  }

  public void setQuantidade(long quantidade) {
    this.quantidade = quantidade;
  }

}
