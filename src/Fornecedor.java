package com.sample;


public class Fornecedor {

  private long idfornecedor;
  private long codpostal;
  private String nome;
  private String numcontabancaria;
  private String email;
  private long numtelefone;
  private String rua;
  private long numporta;


  public long getIdfornecedor() {
    return idfornecedor;
  }

  public void setIdfornecedor(long idfornecedor) {
    this.idfornecedor = idfornecedor;
  }


  public long getCodpostal() {
    return codpostal;
  }

  public void setCodpostal(long codpostal) {
    this.codpostal = codpostal;
  }


  public String getNome() {
    return nome;
  }

  public void setNome(String nome) {
    this.nome = nome;
  }


  public String getNumcontabancaria() {
    return numcontabancaria;
  }

  public void setNumcontabancaria(String numcontabancaria) {
    this.numcontabancaria = numcontabancaria;
  }


  public String getEmail() {
    return email;
  }

  public void setEmail(String email) {
    this.email = email;
  }


  public long getNumtelefone() {
    return numtelefone;
  }

  public void setNumtelefone(long numtelefone) {
    this.numtelefone = numtelefone;
  }


  public String getRua() {
    return rua;
  }

  public void setRua(String rua) {
    this.rua = rua;
  }


  public long getNumporta() {
    return numporta;
  }

  public void setNumporta(long numporta) {
    this.numporta = numporta;
  }

}
